package com.stringlogic;

public class CompareToDemo {

	public static void main(String[] args) {


		String s1="Aman";
		String s2="Abhay";
		
		System.out.println(s1.compareTo(s2));

		//s1 is bigger :positive
		//s1 is smaller:negative
		
		//ascii value difference
		System.out.println("Shweta".compareTo("pratik"));
	}
	

}
